#pragma once

#ifndef MKIN_H
#define MKIN_H

#include <bits/stdc++.h>
using namespace std;

// ═══════════════════════════════════════════════════════════════════
// 总测试点数量 & 测试类型注释
// ═══════════════════════════════════════════════════════════════════
// 25 组测试点，无 subtask 分组，每点独立计分
// 
// 测试类型分布（仅供注释参考）：
//   case 1-2:  样例数据
//   case 3-8:  小规模 + 特殊性质
//   case 9-11: Hack 数据
//   case 12-20: 中大规模
//   case 21-25: 随机回归
// ═══════════════════════════════════════════════════════════════════

const int TEST_CASES = 25;

void test(int case_num, ofstream& fout)
{
    // ============================================================
    // 样例数据 — case 1-2
    // ============================================================
    if (case_num == 1) {
        fout << "5 5" << endl;
        fout << "1 2 3 2 1" << endl;
    }
    else if (case_num == 2) {
        fout << "3 10" << endl;
        fout << "3 4 3" << endl;
    }
    // ============================================================
    // 小规模 + 特殊性质 — case 3-8
    // ============================================================
    else if (case_num == 3) {
        fout << "10 1" << endl;
        for (int i = 0; i < 10; i++) fout << "1 ";
        fout << endl;
    }
    else if (case_num == 4) {
        fout << "1 5" << endl;
        fout << "5" << endl;
    }
    else if (case_num == 5) {
        fout << "1 3" << endl;
        fout << "5" << endl;
    }
    else if (case_num == 6) {
        fout << "5 7" << endl;
        fout << "1 1 1 1 1" << endl;
    }
    else if (case_num == 7) {
        fout << "5 6" << endl;
        fout << "1 2 3 4 5" << endl;
    }
    else if (case_num == 8) {
        fout << "100 1" << endl;
        for (int i = 0; i < 100; i++) fout << "100 ";
        fout << endl;
    }
    // ============================================================
    // Hack 数据 — case 9-11
    // ============================================================
    else if (case_num >= 9 && case_num <= 11) {
        int n = rand() % 9 + 2;
        int k = rand() % 20 + 1;
        fout << n << " " << k << endl;
        for (int i = 0; i < n; i++) fout << (rand() % 20 + 1) << " ";
        fout << endl;
    }
    // ============================================================
    // 中等规模 — case 12-15
    // ============================================================
    else if (case_num >= 12 && case_num <= 15) {
        int n = rand() % 151 + 50;
        int k = rand() % 500 + 1;
        fout << n << " " << k << endl;
        for (int i = 0; i < n; i++) fout << (rand() % 100 + 1) << " ";
        fout << endl;
    }
    // ============================================================
    // 大规模 — case 16-20
    // ============================================================
    else if (case_num >= 16 && case_num <= 20) {
        int n = rand() % 501 + 500;
        int k = rand() % 1000 + 1;
        fout << n << " " << k << endl;
        for (int i = 0; i < n; i++) fout << (rand() % 1000 + 1) << " ";
        fout << endl;
    }
    // ============================================================
    // 随机回归 — case 21-25
    // ============================================================
    else {
        int n = rand() % 201 + 100;
        int k = rand() % 1000 + 1;
        fout << n << " " << k << endl;
        for (int i = 0; i < n; i++) fout << (rand() % 1000 + 1) << " ";
        fout << endl;
    }
}

#endif
