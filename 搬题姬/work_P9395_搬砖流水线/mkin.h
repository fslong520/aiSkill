/**
 * 测试数据生成函数
 * 
 * 参数：
 *   case_num: 测试点编号（1-25）
 *   fout: 输出文件流（写入 .in 文件）
 * 
 * 特殊性质数据设计参考：
 *   - 单调性：递增/递减序列，测试排序、二分等算法
 *   - 所有值相同：测试重复值处理
 *   - 极值：最大/最小可能值，测试溢出
 * 
 * Hack 数据设计参考：
 *   - int 溢出：使用 > 2^31-1 的数据，使 int 错误
 *   - 边界漏判：N=1, N=max 等，测试边界处理
 *   - 超时：使 O(n²) 算法超时
 *   - 错误贪心：使贪心策略失败的数据
 */

void test(int case_num, ofstream& fout)
{
    // ============================================================
    // Subtask 0: 样例数据（直接复制题目样例） — case 1-2
    // ============================================================
    if (case_num == 1)
    {
        // 样例1
        fout << "8" << endl;
        fout << "1 2 3 5 4 6 7 8" << endl;
    }
    else if (case_num == 2)
    {
        // 样例2：单元素
        fout << "1" << endl;
        fout << "100" << endl;
    }
    
    // ============================================================
    // Subtask 1: 小规模随机数据 — case 3-5
    // ============================================================
    else if (case_num >= 3 && case_num <= 5)
    {
        int N = rand() % 10 + 1; // 1-10
        fout << N << endl;
        for (int i = 0; i < N; i++) {
            fout << rand() % 100 + 1; // 1-100
            if (i != N - 1) fout << " ";
        }
        fout << endl;
    }
    
    // ============================================================
    // Subtask 1(续): 特殊性质数据 — case 6-8
    // ============================================================
    else if (case_num == 6)
    {
        // 递增序列（已有序）
        int N = 10;
        fout << N << endl;
        for (int i = 1; i <= N; i++) {
            fout << i;
            if (i != N) fout << " ";
        }
        fout << endl;
    }
    else if (case_num == 7)
    {
        // 所有值相同
        int N = 10;
        fout << N << endl;
        for (int i = 0; i < N; i++) {
            fout << "5";
            if (i != N - 1) fout << " ";
        }
        fout << endl;
    }
    else if (case_num == 8)
    {
        // 递减序列（完全逆序）
        int N = 10;
        fout << N << endl;
        for (int i = N; i >= 1; i--) {
            fout << i;
            if (i != 1) fout << " ";
        }
        fout << endl;
    }
    
    // ============================================================
    // Subtask 2: Hack 数据（针对常见错误写法）— case 9-11
    // ============================================================
    else if (case_num == 9)
    {
        // 边界：N=1
        fout << "1" << endl;
        fout << "1" << endl;
    }
    else if (case_num == 10)
    {
        // 边界：N=2，已有序
        fout << "2" << endl;
        fout << "1 2" << endl;
    }
    else if (case_num == 11)
    {
        // 边界：N=2，逆序
        fout << "2" << endl;
        fout << "2 1" << endl;
    }
    
    // ============================================================
    // Subtask 3: 中等规模数据（基本有序）— case 12-15
    // ============================================================
    else if (case_num >= 12 && case_num <= 15)
    {
        int N = 100;
        fout << N << endl;
        // 生成基本有序的序列，只有少数逆序对
        for (int i = 1; i <= N; i++) {
            fout << i;
            if (i != N) fout << " ";
        }
        // 随机交换几对相邻元素
        int swaps = rand() % 5 + 1; // 1-5次交换
        vector<int> a(N);
        for (int i = 0; i < N; i++) a[i] = i + 1;
        for (int i = 0; i < swaps; i++) {
            int pos = rand() % (N - 1);
            swap(a[pos], a[pos + 1]);
        }
        fout << endl;
        // 重新输出
        fout.clear();
        fout << N << endl;
        for (int i = 0; i < N; i++) {
            fout << a[i];
            if (i != N - 1) fout << " ";
        }
        fout << endl;
    }
    
    // ============================================================
    // Subtask 3(续): 大规模数据（完全随机）— case 16-20
    // ============================================================
    else if (case_num >= 16 && case_num <= 20)
    {
        int N = 100;
        fout << N << endl;
        for (int i = 0; i < N; i++) {
            fout << rand() % 1000 + 1; // 1-1000
            if (i != N - 1) fout << " ";
        }
        fout << endl;
    }
    
    // ============================================================
    // Subtask 4: 随机回归测试 — case 21-25
    // ============================================================
    else
    {
        int N = rand() % 100 + 1; // 1-100
        fout << N << endl;
        for (int i = 0; i < N; i++) {
            fout << rand() % 1000 + 1; // 1-1000
            if (i != N - 1) fout << " ";
        }
        fout << endl;
    }
}

