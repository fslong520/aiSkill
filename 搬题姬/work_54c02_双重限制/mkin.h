#pragma once

#ifndef MKIN_H
#define MKIN_H

#include <bits/stdc++.h>
using namespace std;

// ═══════════════════════════════════════════════════════════════════
// 总测试点数量 & Subtask 分组配置
// ═══════════════════════════════════════════════════════════════════

const int TEST_CASES = 25;

struct SubtaskDef {
    int id, start, end;
};
const SubtaskDef SUBTASKS[] = {
    {0, 1, 2},    // 样例              10分
    {1, 3, 8},    // 小规模 + 特殊性质  20分
    {2, 9, 11},   // Hack 数据          15分
    {3, 12, 20},  // 中大规模           30分
    {4, 21, 25},  // 随机回归           25分
};
const int SUBTASK_COUNT = sizeof(SUBTASKS) / sizeof(SUBTASKS[0]);

void test(int case_num, ofstream& fout)
{
    srand(12345 + case_num * 6789);

    // ────────────────────────────────────────────────────────────
    // Subtask 0: 样例数据（直接复制题目样例） — case 1-2
    // ────────────────────────────────────────────────────────────
    if (case_num == 1)
    {
        fout << "10 15 4\n";
        fout << "3 5 10\n";
        fout << "2 4 7\n";
        fout << "6 3 9\n";
        fout << "1 8 6\n";
    }
    else if (case_num == 2)
    {
        // 3 items, 双约束择二
        fout << "5 5 3\n";
        fout << "2 2 5\n";
        fout << "3 3 6\n";
        fout << "1 1 2\n";
    }

    // ────────────────────────────────────────────────────────────
    // Subtask 1: 小规模随机数据 — case 3-5
    // ────────────────────────────────────────────────────────────
    else if (case_num == 3)
    {
        // N=1，选唯一件
        fout << "10 10 1\n";
        fout << "4 5 8\n";
    }
    else if (case_num == 4)
    {
        // N=3，较小
        fout << "20 20 3\n";
        fout << "5 10 15\n";
        fout << "8 6 12\n";
        fout << "12 5 18\n";
    }
    else if (case_num == 5)
    {
        // N=6，小规模随机
        int W = 30, V = 30, N = 6;
        fout << W << " " << V << " " << N << "\n";
        int w[] = {4, 7, 11, 3, 9, 6};
        int v[] = {8, 5, 4, 12, 16, 9};
        int p[] = {6, 12, 20, 8, 15, 10};
        for (int i = 0; i < N; i++)
            fout << w[i] << " " << v[i] << " " << p[i] << "\n";
    }

    // ────────────────────────────────────────────────────────────
    // Subtask 1(续): 特殊性质数据 — case 6-8
    // ────────────────────────────────────────────────────────────
    else if (case_num == 6)
    {
        // 特殊性质：所有物品 w = v（对称降为 1D 背包）
        int W = 50, V = 50, N = 10;
        fout << W << " " << V << " " << N << "\n";
        for (int i = 0; i < N; i++) {
            int x = rand() % 20 + 1;
            int p = rand() % 50 + 1;
            fout << x << " " << x << " " << p << "\n";
        }
    }
    else if (case_num == 7)
    {
        // 特殊性质：重量紧缺、容积宽裕
        int W = 10, V = 200, N = 15;
        fout << W << " " << V << " " << N << "\n";
        for (int i = 0; i < N; i++) {
            int w = rand() % 8 + 1;
            int v = rand() % 50 + 1;
            int p = rand() % 30 + 1;
            fout << w << " " << v << " " << p << "\n";
        }
    }
    else if (case_num == 8)
    {
        // 特殊性质：唯一单项可装，其余在某一维度超限
        fout << "10 10 5\n";
        fout << "10 10 50\n";   // 恰好填满
        fout << "11 5 40\n";    // 重量超
        fout << "5 11 40\n";    // 容积超
        fout << "12 12 60\n";   // 双超
        fout << "8 8 30\n";     // 可装但价值低
    }

    // ────────────────────────────────────────────────────────────
    // Subtask 2: Hack 数据（针对常见错误写法）— case 9-11
    // ────────────────────────────────────────────────────────────
    else if (case_num == 9)
    {
        // Hack：N=1 但物品超限 → 答案 = 0
        fout << "5 5 1\n";
        fout << "6 6 100\n";
    }
    else if (case_num == 10)
    {
        // Hack：大量极小物品（w=1,v=1），检验小体积大量填充
        int W = 100, V = 100, N = 80;
        fout << W << " " << V << " " << N << "\n";
        for (int i = 0; i < N; i++)
            fout << "1 1 " << (rand() % 10 + 1) << "\n";
    }
    else if (case_num == 11)
    {
        // Hack：按价值密度贪心（p/(w+v)）失败
        // 高密度单品 vs 低密度组合：单品18，组合14+14=28 > 18
        fout << "10 10 4\n";
        fout << "6 6 18\n";
        fout << "5 5 14\n";
        fout << "5 5 14\n";
        fout << "1 1 1\n";
    }

    // ────────────────────────────────────────────────────────────
    // Subtask 3: 中等规模数据 — case 12-15
    // ────────────────────────────────────────────────────────────
    else if (case_num >= 12 && case_num <= 15)
    {
        int sizes[4][3] = {
            {50, 50, 20},
            {100, 100, 30},
            {150, 80, 40},
            {200, 200, 50}
        };
        int idx = case_num - 12;
        int W = sizes[idx][0], V = sizes[idx][1], N = sizes[idx][2];
        fout << W << " " << V << " " << N << "\n";
        for (int i = 0; i < N; i++) {
            int w = rand() % (W * 2 / 3) + 1;
            int v = rand() % (V * 2 / 3) + 1;
            if (w > W) w = W;
            if (v > V) v = V;
            int p = rand() % 150 + 1;
            fout << w << " " << v << " " << p << "\n";
        }
    }

    // ────────────────────────────────────────────────────────────
    // Subtask 3(续): 大规模数据（压力测试）— case 16-20
    // ────────────────────────────────────────────────────────────
    else if (case_num >= 16 && case_num <= 20)
    {
        int W, V, N;
        if (case_num == 16)      { W = 240; V = 240; N = 80; }
        else if (case_num == 17) { W = 250; V = 250; N = 90; }
        else if (case_num == 18) { W = 300; V = 300; N = 100; }
        else if (case_num == 19) { W = 300; V = 300; N = 100; }
        else                     { W = 300; V = 300; N = 100; }

        fout << W << " " << V << " " << N << "\n";
        for (int i = 0; i < N; i++) {
            int w, v, p;
            if (case_num == 19) {
                // 全小件（大量物品可装入）
                w = rand() % 10 + 1;
                v = rand() % 10 + 1;
                p = rand() % 50 + 1;
            } else if (case_num == 20) {
                // 全大件（紧约束）
                w = rand() % 150 + 150;
                v = rand() % 150 + 150;
                p = rand() % 300 + 1;
            } else {
                w = rand() % (W - 1) + 1;
                v = rand() % (V - 1) + 1;
                p = rand() % 300 + 1;
            }
            fout << w << " " << v << " " << p << "\n";
        }
    }

    // ────────────────────────────────────────────────────────────
    // Subtask 4: 随机回归测试 — case 21-25
    // ────────────────────────────────────────────────────────────
    else
    {
        int W = rand() % 300 + 1;
        int V = rand() % 300 + 1;
        int N = rand() % 100 + 1;
        fout << W << " " << V << " " << N << "\n";
        for (int i = 0; i < N; i++) {
            int w = rand() % W + 1;
            int v = rand() % V + 1;
            int p = rand() % 300 + 1;
            fout << w << " " << v << " " << p << "\n";
        }
    }
}

#endif
