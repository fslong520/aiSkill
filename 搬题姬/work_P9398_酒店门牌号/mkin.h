/**
 * 测试数据生成函数
 * 
 * 参数：
 *   case_num: 测试点编号（1-25）
 *   fout: 输出文件流（写入 .in 文件）
 * 
 * 本题背景：枚举 100~999，统计各位数字之和等于 k 的个数。
 * 数据只需要一个整数 k（1 ≤ k ≤ 27）。
 */

void test(int case_num, ofstream& fout)
{
    // ============================================================
    // 样例数据 — case 1
    // ============================================================
    if (case_num == 1)
    {
        fout << "6" << endl;
    }

    // ============================================================
    // 边界对称测试（覆盖最小、最大及各边界值）— case 2-11
    // 精心选择成对数值验证代码正确性
    // ============================================================
    else if (case_num == 2)
    {
        // k=1：仅 100 → 1 个风水房
        fout << "1" << endl;
    }
    else if (case_num == 3)
    {
        // k=27：仅 999 → 1 个风水房
        fout << "27" << endl;
    }
    else if (case_num == 4)
    {
        // k=2：100+010+001=2 → 3个（101,110,200）
        fout << "2" << endl;
    }
    else if (case_num == 5)
    {
        // k=26：998,989,899 → 3个
        fout << "26" << endl;
    }
    else if (case_num == 6)
    {
        fout << "3" << endl;
    }
    else if (case_num == 7)
    {
        fout << "25" << endl;
    }
    else if (case_num == 8)
    {
        fout << "4" << endl;
    }
    else if (case_num == 9)
    {
        fout << "24" << endl;
    }
    else if (case_num == 10)
    {
        fout << "5" << endl;
    }
    else if (case_num == 11)
    {
        // k=23：次大值附近
        fout << "23" << endl;
    }

    // ============================================================
    // 随机数据 — case 12-25
    // 覆盖 k 在 [1,27] 范围内的各种取值
    // ============================================================
    else if (case_num >= 12 && case_num <= 15)
    {
        fout << (rand() % 27 + 1) << endl;
    }
    else if (case_num >= 16 && case_num <= 20)
    {
        fout << (rand() % 27 + 1) << endl;
    }
    else
    {
        fout << (rand() % 27 + 1) << endl;
    }
}

